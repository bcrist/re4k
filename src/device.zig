pub const DeviceFamily = enum {
    low_power, // V/B/C suffix
    zero_power, // ZC suffix
    zero_power_enhanced, // ZE suffix

    pub fn getPartNumberSuffix(self: DeviceFamily) []const u8 {
        return switch (self) {
            .low_power => "V", // or C or B
            .zero_power => "ZC",
            .zero_power_enhanced => "ZE",
        };
    }
};

