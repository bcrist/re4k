pub const MiscPinType = enum {
    no_connect,
    gnd,
    vcc_core,
    vcco_bank_0,
    vcco_bank_1,
    tck,
    tms,
    tdi,
    tdo,
};

pub const PinInfo = union(enum) {
    input: struct {
        pin_index: u16,
        pin_number: []const u8,
        bank: u8,
        glb: u8,
    },
    clock_input: struct {
        pin_index: u16,
        pin_number: []const u8,
        bank: u8,
        glb: u8,
        clock_index: u8,
    },
    input_output: struct {
        pin_index: u16,
        pin_number: []const u8,
        bank: u8,
        glb: u8,
        mc: u8,
        is_goe: bool,
    },
    misc: struct {
        pin_index: u16,
        pin_number: []const u8,
        misc_type: MiscPinType,
    },

    pub fn pin_index(self: PinInfo) u16 {
        return switch (self) {
            .input => |info| info.pin_index,
            .clock_input => |info| info.pin_index,
            .input_output => |info| info.pin_index,
            .misc => |info| info.pin_index,
        };
    }

    pub fn pin_number(self: PinInfo) []const u8 {
        return switch (self) {
            .input => |info| info.pin_number,
            .clock_input => |info| info.pin_number,
            .input_output => |info| info.pin_number,
            .misc => |info| info.pin_number,
        };
    }
};

fn _pin_number(comptime pin_index: u16, pin_number: ?[]const u8) []const u8 {
    return pin_number orelse std.fmt.comptimePrint("{}", .{ pin_index + 1 });
}

fn _in(comptime pin_index: u16, pin_number: ?[]const u8, bank: u8, glb: u8) PinInfo {
    return .{ .input = .{
        .pin_index = pin_index,
        .pin_number = _pin_number(pin_index, pin_number),
        .bank = bank,
        .glb = glb,
    }};
}

fn _clk(comptime pin_index: u16, pin_number: ?[]const u8, bank: u8, glb: u8, clk_index: u8) PinInfo {
    return .{ .clock_input = .{
        .pin_index = pin_index,
        .pin_number = _pin_number(pin_index, pin_number),
        .bank = bank,
        .glb = glb,
        .clock_index = clk_index,
    }};
}

fn _io(comptime pin_index: u16, pin_number: ?[]const u8, bank: u8, glb: u8, mc: u8) PinInfo {
    return .{ .input_output = .{
        .pin_index = pin_index,
        .pin_number = _pin_number(pin_index, pin_number),
        .bank = bank,
        .glb = glb,
        .mc = mc,
        .is_goe = false,
    }};
}

fn _goe(comptime pin_index: u16, pin_number: ?[]const u8, bank: u8, glb: u8, mc: u8) PinInfo {
    return .{ .input_output = .{
        .pin_index = pin_index,
        .pin_number = _pin_number(pin_index, pin_number),
        .bank = bank,
        .glb = glb,
        .mc = mc,
        .is_goe = true,
    }};
}

fn _misc(comptime pin_index: u16, pin_number: ?[]const u8, misc_type: MiscPinType) PinInfo {
    return .{ .misc = .{
        .pin_index = pin_index,
        .pin_number = _pin_number(pin_index, pin_number),
        .misc_type = misc_type,
    }};
